#include "jr_atoi.h"

int	jr_atoi(char *str)
{
	int	num;	
	int	i;

	if (!str)
		return (0);
	while (str[i] == '-' || (str[i] >= 9 && str[i] <= 13))
		i++;
	num = 0;
	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	return (num);
}
