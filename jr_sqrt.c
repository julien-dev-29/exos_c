#include "jr_sqrt.h"

int	jr_sqrt(int nb)
{
	double	i;

	if (nb <= 0)
		return (0);
	i = 1;
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
